const displaySubmissions = (list) => {
  const submissionsContainer = document.getElementById("submissions-container");
  const allImageSets = list.map((item) => {
    return `<div class="submissionsContainer">
      <img src=${item.images[0]} alt=${item.title} class="submissionsImage" />
      <div class="submissionsInfo">
        <p class="submissionsName">${item.name} - ${item.title}</p>
        <a href="details.html?id=${item.id}"><p class="submissionsTitle">see more</p></a>
      </div>
    </div>`
  }).join("");

  submissionsContainer.innerHTML = allImageSets;
}

window.onload = function(){
  displaySubmissions(imageSets);
}

// Submissions Button

const submitBtn = document.getElementById("submit-btn");

submitBtn.addEventListener("click", (e) => {
  e.preventDefault();
  const name = document.querySelector("#name").value;
  const title = document.querySelector("#title").value;
  const url1 = document.querySelector("#url1").value;
  const url2 = document.querySelector("#url2").value;
  const url3 = document.querySelector("#url3").value;
  const url4 = document.querySelector("#url4").value;
  const url5 = document.querySelector("#url5").value;
  const images = [url1, url2, url3, url4, url5];
  const highestId = Math.max(...imageSets.map(obj => obj.id));
  const nextId = highestId + 1;
  const submission = {id:nextId, name:name, title:title, images:images};
  imageSets.unshift(submission);

  console.log(imageSets);
  displaySubmissions(imageSets);

  document.querySelector("#name").value = "";
  document.querySelector("#title").value = "";
  document.querySelector("#url1").value = "";
  document.querySelector("#url2").value = "";
  document.querySelector("#url3").value = "";
  document.querySelector("#url4").value = "";
  document.querySelector("#url5").value = "";
});

